let string_of_charlist char_list =
  char_list |> List.map (fun ch -> String.make 1 ch) |> String.concat ""

let read_lines filename =
  let lines = ref [] in
  let chan = open_in filename in
  try
    while true do
      lines := input_line chan :: !lines
    done;
    !lines
  with End_of_file ->
    close_in chan;
    List.rev !lines

let spacesep ns = String.concat " " ns